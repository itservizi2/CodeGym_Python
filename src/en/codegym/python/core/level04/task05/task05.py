# 8th Grade Math

# Write a program that asks the user for a number and uses the math library to calculate and display
# the square root and factorial of that number.

import math
number = int(input("INSERT A NUMBER  "))
square_root = math.sqrt(number)
factorial_value = math.factorial(number)
print(f"factorial value is {factorial_value} and square root value is {square_root}")
