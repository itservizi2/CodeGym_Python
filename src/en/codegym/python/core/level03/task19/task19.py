# F-notation

# Write a program that asks the user for their name, age, and city.
# Then use f-notation to display a message in the following format: "Hello, {name}! You are {age} years old, and you live in {city}."

print("Your name is : ")
name=input()
print("Your age is : ")
age=int(input())
print("Your city is : ")
city=input()
print(f"Hello, {name}! You are {age} years old, and you live in {city}.")
