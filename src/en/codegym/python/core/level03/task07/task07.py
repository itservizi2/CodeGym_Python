# Age User
# Write a program that asks the user for their current age (an integer) and outputs their age 50 years from now.
# Use the int() function to convert the input.
# Example:
# Enter your current age: 20
# In 50 years, you will be 70 years old.

print("Insert your age ")
age = int(input())
future_age = age + 50
print(f"In 50 years, you will be {future_age} years old.")
